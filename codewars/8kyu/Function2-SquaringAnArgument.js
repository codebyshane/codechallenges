// Challenge:
// Function 2 - squaring an argument


// Now you have to write a function that takes an argument and returns the square of it.


// PREP
// Parameters:
// number

// Results:
// number

// Examples:
// 3 --> 9
// 2 --> 4
// 12 --> 144


// Solution with Psuedo: 

function square(x){
    return Math.pow(x, 2);
}