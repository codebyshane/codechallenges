// Challenge:
// Switch it Up!


// When provided with a number between 0-9, return it in words.

// Input :: 1

// Output :: "One".

// If your language supports it, try using a switch statement.


// PREP
// Parameters:
// number

// Results:
// string

// Examples:
// 1 --> "One"
// 3 --> "Three"
// 7 --> "Seven"


// Solution with Psuedo: 

function switchItUp(number){
    switch (number) {
        case 1: return "One"
        case 2: return "Two"
        case 3: return "Three"
        case 4: return "Four"
        case 5: return "Five"
        case 6: return "Six"
        case 7: return "Seven"
        case 8: return "Eight"
        case 9: return "Nine"
        case 0: return "Zero"
      }
    }