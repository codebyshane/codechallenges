//We need a function that can transform a number into a string.

//What ways of achieving this do you know?



//PREP
//Parameters:
//integer

//Results:
//string

//Examples:
//123 --> "123"
//999 --> "999"
//14000605 --> "14000605"

//Solution with Psuedo:

function numberToString(num) {
    //use .toString because it is built in and automatically converts the parameter to a string
    return num.toString();
  }

numberToString(123)
